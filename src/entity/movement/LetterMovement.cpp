constexpr float MAX_MAGNITUDE = 0.005f;

LetterMovement::LetterMovement(char letter): _position(0.f), _scale(10.f) {
	init_points(letter);
}

void LetterMovement::apply_force(std::vector<Movable>& movables) {
	for (Movable& m : movables) {
		apply_force(&m);
	}
}

void LetterMovement::apply_force(Movable* movable) {
	glm::mat4 model(1.f);
	model = glm::translate(model, _position);
	model = glm::scale(model, glm::vec3(_scale));
	glm::vec4 target_point = model * _points[0];
	for (const glm::vec4& point : _points) {
		glm::vec4 translated_point = model * point;
		glm::vec4 movable_pos(movable->get_position(), 1.f);
		if (glm::distance2(translated_point, movable_pos) < glm::distance2(target_point, movable_pos)) {
			target_point = translated_point;
		}
	}

	glm::vec3 dir = glm::vec3(target_point.x, target_point.y, target_point.z) - movable->get_position();
	glm::vec3 update = dir - movable->get_velocity();

	if (glm::length(update) > MAX_MAGNITUDE) {
		update *= MAX_MAGNITUDE / glm::length(update);
	}

	movable->update_acceleration(update);
}

